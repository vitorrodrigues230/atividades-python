valor_1=float(input("selecione o primeiro valor: "))
valor_2=float(input("selecione o segundo valor: "))
n=input("Selecione entre as operações basicas +, -, *, /: ")
if n == "*":
    resultado=valor_1*valor_2
    print(f'A multiplicação dos valores {valor_1} e {valor_2} é igual A {resultado}')
elif n == "+":
    resultado=valor_1+valor_2
    print(f'A soma dos valores {valor_1} e {valor_2} é igual A {resultado}')
elif n == "-":
    resultado=valor_1-valor_2
    print(f'A subtração dos valores {valor_1} e {valor_2} é igual A {resultado}')
elif n == "/":
    resultado=valor_1/valor_2
    print(f'A divisão dos valores {valor_1} e {valor_2} é igual A {resultado}')










